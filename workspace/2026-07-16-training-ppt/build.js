async function build() {
  for (let i = 1; i <= 13; i++) {
    const n = String(i).padStart(2, '0');
    const file = `slides/slide-${n}.html`;
    const { slide } = await html2pptx(file, pptx);
    if (notes[i]) slide.addNotes(notes[i]);
    console.log(`  ✓ slide ${i}/13`);
  }
  await pptx.writeFile({ fileName: '数据驱动运营与人才分层辅导.pptx' });
  console.log('✓ Saved 数据驱动运营与人才分层辅导.pptx');
}

build().catch(e => { console.error(e); process.exit(1); });

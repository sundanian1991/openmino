def get_source_for_section(filename, section_title):
    """根据文件名和节标题获取来源"""
    # 先查精确映射
    if filename in SECTION_SOURCE_MAP:
        for pattern, source in SECTION_SOURCE_MAP[filename].items():
            if re.search(pattern, section_title):
                return source
    # 返回默认来源
    return SOURCE_MAP.get(filename, "[待补充来源]")

def annotate_file(filepath):
    """为单个文件添加来源标注"""
    content = filepath.read_text(encoding="utf-8")
    lines = content.splitlines()
    filename = filepath.name

    new_lines = []
    i = 0
    added_count = 0

    while i < len(lines):
        line = lines[i]
        new_lines.append(line)

        # 匹配二级/三级标题
        section_match = re.match(r'^(#{2,3})\s+(.+)$', line)
        if section_match:
            level = len(section_match.group(1))
            title = section_match.group(2)

            # 跳过已经标注了来源的节、以及总览/索引类节
            skip_keywords = ['来源', '编码', '核心定位', '总览', '索引', '导航', '速查卡', '关联矩阵', '心法', '目录']
            if any(kw in title for kw in skip_keywords):
                i += 1
                continue

            # 检查标题后是否已经存在 [来源] 或 空行后就是另一个标题
            source_already_exists = False
            j = i + 1
            empty_lines = 0
            while j < len(lines) and j < i + 5:
                next_line = lines[j].strip()
                if not next_line:
                    empty_lines += 1
                elif '[来源' in next_line or '来源：' in next_line:
                    source_already_exists = True
                    break
                elif next_line.startswith('#'):
                    break
                elif empty_lines >= 1 and (next_line.startswith('|') or next_line.startswith('- ') or next_line.startswith('1.')):
                    # 空行后是内容，检查来源标注
                    pass
                j += 1

            if source_already_exists:
                i += 1
                continue

            # 获取来源
            source = get_source_for_section(filename, title)

            # 在标题后添加空行+来源标注
            # 先跳过现有的空行
            j = i + 1
            while j < len(lines) and not lines[j].strip():
                new_lines.append(lines[j])
                j += 1

            new_lines.append(f"> [来源：{source}]")
            new_lines.append("")
            added_count += 1
            i = j - 1  # 循环会i+=1，所以这里减1

        i += 1

    # 写回文件
    filepath.write_text("\n".join(new_lines), encoding="utf-8")
    return added_count

def main():
    print("🔧 开始自动补充来源标注...\n")
    total_added = 0

    for filename in sorted(SOURCE_MAP.keys()):
        filepath = DISTILL_DIR / filename
        if not filepath.exists():
            print(f"  ⚠️ 文件不存在: {filename}")
            continue

        print(f"  处理: {filename}")
        added = annotate_file(filepath)
        print(f"    新增 {added} 个来源标注")
        total_added += added

    print(f"\n✅ 完成！共新增 {total_added} 个来源标注")

if __name__ == "__main__":
    main()

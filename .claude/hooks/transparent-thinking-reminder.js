#!/usr/bin/env node

/**
 * Transparent Thinking Reminder — 显性思维提醒器
 *
 * 触发时机：UserPromptSubmit Hook（用户发送消息时）
 *
 * 功能：
 * 1. 提醒 AI 使用显性思维
 * 2. 不检测复杂度，只做提醒
 *
 * 输出：简洁的提醒消息
 */

function generateReminder() {
    const timestamp = new Date().toISOString().slice(0, 19).replace('T', ' ');

    return `
┌──────────────────────────────────────────────────────────────────────┐
│  🧠 显性思维提醒                                                      │
├──────────────────────────────────────────────────────────────────────┤
│  回复前输出：                                                          │
│  【我在做什么】[意图] + [行动]                                         │
│                                                                       │
│  意图分类：                                                            │
│  • 简单问答 → 直接回答                                                 │
│  • 复杂任务 → 拆分步骤                                                 │
│  • 探索搜索 → 声明工具                                                 │
│  • 代码修改 → 说明范围                                                 │
└──────────────────────────────────────────────────────────────────────┘
${timestamp}
`;
}

console.log(generateReminder());
process.exit(0);